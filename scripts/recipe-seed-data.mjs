function ingredient(
  ingredientName,
  quantity,
  unit = "g",
  ingredientRole = "main",
  scalingMode = "linear",
  displayQuantity,
  displayUnit,
) {
  return {
    ingredientName,
    quantity,
    unit,
    ingredientRole,
    scalingMode,
    ...(displayQuantity !== undefined ? { displayQuantity: String(displayQuantity) } : {}),
    ...(displayUnit !== undefined ? { displayUnit } : {}),
  };
}

function textIngredient(ingredientName, displayQuantity = "maun mukaan", displayUnit = "") {
  return ingredient(ingredientName, undefined, "g", "spice", "text_only", displayQuantity, displayUnit);
}

function fixedIngredient(ingredientName, quantity, unit = "g", displayQuantity, displayUnit, ingredientRole = "spice") {
  return ingredient(ingredientName, quantity, unit, ingredientRole, "fixed", displayQuantity, displayUnit);
}

function numbered(steps) {
  return steps.map((step, index) => `${index + 1}. ${step}`).join("\n");
}

function recipe({
  name,
  description,
  mealTag,
  dietaryFlags = [],
  allergies = [],
  ingredients,
  instructions,
  defaultServings,
  minServings,
  maxServings,
}) {
  return {
    name,
    description,
    mealTag,
    dietaryFlags,
    allergies,
    instructions: numbered(instructions),
    defaultServings,
    minServings,
    maxServings,
    ingredients,
  };
}

